namespace Mercedes.Data.Enums;

public enum CarType
{
    /// <summary>
    /// Неизвестный тип
    /// </summary>
    Unknown = 0,

    /// <summary>
    /// Седан - классический трехобъемный кузов, багажник отделен от салона
    /// </summary>
    Sedan = 1,

    /// <summary>
    /// Универсал - удлиненная версия с большой дверью сзади
    /// </summary>
    Estate = 2,

    /// <summary>
    /// Хэтчбек - задняя дверь вместе со стеклом, короткий задний свес
    /// </summary>
    Hatchback = 3,

    /// <summary>
    /// Внедорожник / SUV - высокий клиренс, полный привод, рамная конструкция
    /// </summary>
    SUV = 4,

    /// <summary>
    /// Купе - спортивный дизайн, покатая крыша, обычно 2 двери
    /// </summary>
    Coupe = 5,

    /// <summary>
    /// Кабриолет - мягкая складная крыша
    /// </summary>
    Cabriolet = 6,

    /// <summary>
    /// Родстер - двухместный спортивный автомобиль с открытым верхом
    /// </summary>
    Roadster = 7,

    /// <summary>
    /// Минивэн - однообъемный кузов, вместительный пассажирский
    /// </summary>
    Minivan = 8,

    /// <summary>
    /// Пикап - грузовая платформа, отделенная от кабины
    /// </summary>
    Pickup = 9,

    /// <summary>
    /// Лимузин - удлиненная версия седана для VIP-перевозок
    /// </summary>
    Limousine = 10,

    /// <summary>
    /// Универсал (альтернативное название Estate)
    /// </summary>
    Wagon = 11,

    /// <summary>
    /// Кроссовер - SUV на легковой платформе
    /// </summary>
    Crossover = 12,

    /// <summary>
    /// Лифтбек - гибрид седана и хэтчбека
    /// </summary>
    Liftback = 13,

    /// <summary>
    /// Фастбек - покатая крыша, плавно переходящая в багажник
    /// </summary>
    Fastback = 14,

    /// <summary>
    /// Конвертибл - автомобиль с жесткой складывающейся крышей
    /// </summary>
    Convertible = 15,

    /// <summary>
    /// Фургон - грузовой цельнометаллический кузов
    /// </summary>
    Van = 16
}